    private void guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMouseClicked
        // TODO add your handling code here:
        if(!this.id.getText().isEmpty() && !this.nom.getText().isEmpty() && !this.cod.getText().isEmpty() && !this.tam.getText().isEmpty() && !this.dist.getText().isEmpty() && !this.mas.getText().isEmpty()){
            if(this.ctrl.insertL(nom.getText(), cod.getText(), Float.parseFloat(this.tam.getText()), Float.parseFloat(mas.getText()), Float.parseFloat(this.dist.getText()), Integer.parseInt(this.id.getText()))){
                JOptionPane.showMessageDialog(null, "Datos registrados");
                this.ctrl.bubbleDist();
                this.clear();
            }else{
                JOptionPane.showMessageDialog(null, "No se registraron los datos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe diligenciar todos los campos");
        }
        this.resp.setText(this.ctrl.showAll());
        this.mediaMasa.setText(Float.toString(this.ctrl.mediaMasa()));
    }//GEN-LAST:event_guardarMouseClicked

