    private void eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMouseClicked
        // TODO add your handling code here:
        if(!this.id.getText().isEmpty() ){
            if(this.ctrl.delete(Integer.parseInt(this.id.getText()))){
                JOptionPane.showMessageDialog(null, "Datos eliminados");
                this.clear();
            }else{
                JOptionPane.showMessageDialog(null, "No se eliminaron los datos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe diligenciar el campo id");
        }
        this.resp.setText(this.ctrl.showAll());
        this.mediaMasa.setText(Float.toString(this.ctrl.mediaMasa()));
    }//GEN-LAST:event_eliminarMouseClicked

    private void ActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ActualizarMouseClicked
        // TODO add your handling code here:
        if(!this.id.getText().isEmpty() && !this.nom.getText().isEmpty() && !this.cod.getText().isEmpty() && !this.tam.getText().isEmpty() && !this.dist.getText().isEmpty() && !this.mas.getText().isEmpty()){
            if(this.ctrl.update(nom.getText(), cod.getText(), Float.parseFloat(this.tam.getText()), Float.parseFloat(mas.getText()), Float.parseFloat(this.dist.getText()), Integer.parseInt(this.id.getText()))){
                JOptionPane.showMessageDialog(null, "Datos actualizados");
                this.clear();
            }else{
                JOptionPane.showMessageDialog(null, "No se actualizaron los datos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe diligenciar todos los campos");
        }
        this.resp.setText(this.ctrl.showAll());
        this.mediaMasa.setText(Float.toString(this.ctrl.mediaMasa()));
    }//GEN-LAST:event_ActualizarMouseClicked

    private void buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarMouseClicked
        // TODO add your handling code here:
        if(!this.id.getText().isEmpty() ){
            String p = this.ctrl.select(Integer.parseInt(this.id.getText()));
            if(p != null){
                JOptionPane.showMessageDialog(null, "Datos encontrados");
                this.eliminar.setVisible(true);
                this.Actualizar.setVisible(true);
                this.id.setEditable(false);
                this.id.setEnabled(false);
                this.id.setText(p.split(",")[0]);
                this.nom.setText(p.split(",")[1]);
                this.cod.setText(p.split(",")[2]);
                this.tam.setText(p.split(",")[3]);
                this.mas.setText(p.split(",")[4]);
                this.dist.setText(p.split(",")[5]);
            }else{
                JOptionPane.showMessageDialog(null, "No se encontraron los datos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe diligenciar el campo id");
        }
        this.resp.setText(this.ctrl.showAll());
        this.mediaMasa.setText(Float.toString(this.ctrl.mediaMasa()));
    }//GEN-LAST:event_buscarMouseClicked


